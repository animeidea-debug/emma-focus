#!/bin/sh

# ==============================================================================
# 🚀 极空间监控合并总控脚本：串行排队执行 (调整后：先小米，后萤石)
#
# 部署路径：将整个 video-merge 目录拷贝到 NAS 的脚本目录，
#           如 /tmp/zfsv3/nvme14/13918962622/data/scripts/
# 定时触发：crontab 每日 22:00
#           $ crontab -e
#           0 22 * * * cd /path/to/scripts && sh run_all.sh >> /var/log/video_merge.log 2>&1
# ==============================================================================

# 自动获取脚本所在目录（无论从何处调用都正确定位同级脚本）
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

echo "================================================="
echo " 🚀 开始执行监控视频自动合并任务 | $(date)"
echo "================================================="

# 第一步：集中全部算力执行小米（720P）任务
echo ""
echo "▶️ [1/2] 正在启动【小米】合并任务..."
sh "$SCRIPT_DIR/auto_merge.sh"

MIAMI_EXIT=$?

# 第二步：无论小米是否成功，都继续执行萤石任务（失败时记录但不阻塞）
if [ $MIAMI_EXIT -ne 0 ]; then
    echo "⚠️ 小米任务返回非零状态 ($MIAMI_EXIT)，继续执行萤石任务..."
fi
echo ""
echo "▶️ [2/2] 正在启动【萤石】合并任务..."
sh "$SCRIPT_DIR/yingshi_auto_merge.sh"

echo ""
echo "✅ 所有监控视频延时处理全部完毕！"
echo "🎉 结束时间: $(date)"
echo "================================================="
